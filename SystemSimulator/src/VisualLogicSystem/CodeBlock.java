package VisualLogicSystem;


public class CodeBlock {
    
    
    public int ID;
    public String compileCode = "";
      

    public String getCompileCode() {
        return compileCode;
    }
    

    public int getID() {
        return ID;
    }

    public void setCompileCode(String compileCode) {
        this.compileCode = compileCode;
    }

    public void setID(int ID) {
        this.ID = ID;
    }
}
